/*=============== CHANGE HEADER STYLES ===============*/
const scrollHeader = () =>{
   const header = document.getElementById('header')
   
   this.scrollY >= 50 ? header.classList.add('shadow-header') 
                      : header.classList.remove('shadow-header')
}
window.addEventListener('scroll', scrollHeader)

/*=============== SHOW SCROLL UP ===============*/
const scrollUp = () =>{
	const scrollUp = document.getElementById('scroll-up')
   
	this.scrollY >= 350 ? scrollUp.classList.add('show-scroll')
						     : scrollUp.classList.remove('show-scroll')
}
window.addEventListener('scroll', scrollUp)

/*=============== SCROLL SECTIONS ACTIVE LINK ===============*/
const sections = document.querySelectorAll('section[id]')

const scrollActive = () => {
   const scrollDown = window.scrollY

   sections.forEach(current => {
      const sectionHeight = current.offsetHeight, // id of each section
            sectionTop = current.offsetTop - 50, // Distance from the top edge
            sectionId = current.getAttribute('id'), // Element height
            sectionClass = document.querySelector('.nav__menu a[href*=' + sectionId + ']') // id nav link

      if(scrollDown > sectionTop && scrollDown <= sectionTop + sectionHeight){
         sectionClass.classList.add('active-link')
      }else{
         sectionClass.classList.remove('active-link')
      }
   })
}
window.addEventListener('scroll', scrollActive)

